public class VariablesIntro { // <-- class
    public static void main(String[] args) { // <-- method

        int age = 16; // primitive data type
        double gpa = 3.75; // primitive data type
        boolean passed = true; // primitive data type
        char grade = 'A'; // primitive data type
        String name = "3621"; // non-primitive data type!!

        System.out.println(age);
        System.out.println(gpa);
        System.out.println(passed);
        System.out.println(grade);
        System.out.println(name);

    }
}
